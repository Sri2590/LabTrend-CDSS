from fastapi import APIRouter, HTTPException, Depends
from fastapi.responses import HTMLResponse
from sqlalchemy.orm import Session
from typing import List
from app.schemas.patient import Patient, PatientCreate
from app.database import get_db, PatientDB, LabResultDB
from app import store
import datetime

router = APIRouter()

def _next_available_id(db: Session) -> str:
    all_patients = db.query(PatientDB).all()
    nums = set()
    for p in all_patients:
        pid = str(p.id)
        if pid.startswith("P") and pid[1:].isdigit():
            nums.add(int(pid[1:]))
    n = 1
    while n in nums:
        n += 1
    return f"P{str(n).zfill(3)}"

# ── SPECIFIC routes first ─────────────────────────────────────

@router.get("/next-id")
def get_next_patient_id(db: Session = Depends(get_db)):
    return {"next_id": _next_available_id(db)}

@router.get("/", response_model=List[Patient])
def list_patients(search: str = "", db: Session = Depends(get_db)):
    q = db.query(PatientDB)
    if search:
        q = q.filter(
            PatientDB.name.ilike(f"%{search}%") |
            PatientDB.id.ilike(f"%{search}%")
        )
    return q.all()

@router.post("/", response_model=Patient)
def create_patient(data: PatientCreate, db: Session = Depends(get_db)):
    if data.id:
        if db.query(PatientDB).filter(PatientDB.id == data.id).first():
            raise HTTPException(status_code=400, detail="Patient ID already exists")
        patient_id = data.id
    else:
        patient_id = _next_available_id(db)
    p = PatientDB(
        id=patient_id, name=data.name, age=data.age, gender=data.gender,
        risk="Unknown", dob=data.dob, contact=data.contact, address=data.address,
    )
    db.add(p)
    db.commit()
    db.refresh(p)
    store.add_audit_log("CREATE_PATIENT", "clinician", f"Created patient {patient_id} — {data.name}")
    return p

