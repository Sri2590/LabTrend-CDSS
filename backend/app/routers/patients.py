@router.get("/{patient_id}", response_model=Patient)
def get_patient(patient_id: str, db: Session = Depends(get_db)):
    p = db.query(PatientDB).filter(PatientDB.id == patient_id).first()
    if not p:
        raise HTTPException(status_code=404, detail="Patient not found")
    return p

@router.put("/{patient_id}", response_model=Patient)
def update_patient(patient_id: str, data: PatientCreate, db: Session = Depends(get_db)):
    p = db.query(PatientDB).filter(PatientDB.id == patient_id).first()
    if not p:
        raise HTTPException(status_code=404, detail="Patient not found")
    for key, value in data.dict().items():
        if value is not None:
            setattr(p, key, value)
    db.commit()
    db.refresh(p)
    store.add_audit_log("EDIT_PATIENT", "clinician", f"Updated patient {patient_id} — {str(p.name)}")
    return p